package chapter6;

// Этот класс реализует стек целых чисел, который может хранить 10 элементов
class Stack {
    int[] stck = new int[10];
    int tos;

    // Инициализировать верхушку стека.
    Stack() {
        tos = -1;
    }

    // Поместить элемент в стек.
    void push(int item) {
        if(tos == 9)
            System.out.println("Стек полон.");
        else
            stck[++tos] = item;
    }

    // Извлечь элемент из стека.
    int pop() {
        if(tos < 0) {
            System.out.println("Стек опустошен.");
            return 0;
        }
        else
            return stck[tos--];
    }
}
/*Что демонстрирует код (реализация стека):
Класс Stack реализует структуру данных LIFO (Last In, First Out — последним пришёл, первым вышел):

Поля: stck — массив для хранения элементов (10 элементов), tos (top of stack) — индекс вершины стека
Конструктор: инициализирует tos = -1 (стек пуст)
Метод push(): добавляет элемент на вершину стека (предварительно увеличивая tos)
Метод pop(): удаляет и возвращает элемент с вершины (уменьшая tos)
*/

