// Демонстрация использования аргументов переменной длины.
class VarArgs {
    // vaTest() использует аргументы переменной длины
    static void vaTest(int ... v) {
        System.out.print("Количество аргументов: " + v.length + " Содержимое: ");
        for(int x : v)
            System.out.print(x + " ");
        System.out.println();
    }

    public static void main(String[] args) {
        // Обратите внимание, что теперь метод vaTest()
        // можно вызывать с переменным числом аргументов.
        vaTest(10);       // 1 аргумент
        vaTest(1, 2, 3);  // 3 аргумента
        vaTest();         // без аргументов
    }
}
/*
Что демонстрирует код (varargs — аргументы переменной длины):
Синтаксис int... v (троеточие) позволяет методу принимать произвольное количество аргументов указанного типа.

Как это работает:
Компилятор автоматически упаковывает переданные аргументы в массив
Внутри метода к аргументам можно обращаться как к массиву v
Метод можно вызывать с любым количеством аргументов (включая ноль)*/

