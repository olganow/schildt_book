package chapter7;

// Демонстрация применения статических переменных, методов и блоков.
class UseStatic {
    static int a = 3;
    static int b;

    static void meth(int x) {
        System.out.println("x = " + x);
        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }

    static {
        System.out.println("Инициализация в статическом блоке.");
        b = a * 4;
    }

    public static void main(String[] args) {
        meth(42);
    }
}
/*
Что демонстрирует код (статические члены класса):

Элемент	Описание
static int a = 3	статическая переменная, инициализируется при загрузке класса
static int b	статическая переменная без начального значения
static { ... }	статический блок инициализации (выполняется один раз при загрузке класса)
static void meth()	статический метод (можно вызывать без создания объекта)
Порядок выполнения:

Загрузка класса UseStatic → выполняется статический блок: вывод "Инициализация в статическом блоке.", b = 3 * 4 = 12

Вызов main() → вызов meth(42)
*/
