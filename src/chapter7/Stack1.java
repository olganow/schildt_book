package chapter7;

// Этот класс реализует стек целых чисел, который может хранить 10 значений
// стр 197
class Stack1 {
    /* Теперь stck и tos являются закрытыми. Это значит,
    что они не могут быть случайно или злонамеренно
    изменены таким образом, что может повредиться стек.
    */

    private int[] stck = new int[10];
    private int tos;

    // Инициализировать верхушку стека.
    Stack1() {
        tos = -1;
    }

    // Поместить элемент в стек.
    void push(int item) {
        if(tos == 9)
            System.out.println("Стек полон.");
        else
            stck[++tos] = item;
    }

    // Извлечь элемент из стека.
    int pop() {
        if(tos < 0) {
            System.out.println("Стек опустошен.");
            return 0;
        }
        else
            return stck[tos--];
    }
}
/*
Что демонстрирует код (инкапсуляция с помощью private):
Массив stck и индекс tos объявлены как private, что делает их недоступными извне класса.
Это защищает внутреннее состояние стека от некорректного изменения.

Преимущества инкапсуляции:
Данные скрыты от внешнего кода
Доступ к данным возможен только через методы push() и pop()
Внутренняя реализация может быть изменена без влияния на код, использующий класс
*/
