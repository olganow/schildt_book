package chapter7;

// Объекты передаются через ссылки на них.
class Test3 {
    int a, b;
    Test3(int i, int j) {
        a = i;
        b = j;
    }
    // Передать объект.
    void meth(Test3 o) {
        o.a *= 2;
        o.b /= 2;
    }
}

class PassObjRef {
    public static void main(String[] args) {
        Test3 ob = new Test3(15, 20);

        System.out.println("ob.a и ob.b перед вызовом: " +
                ob.a + " " + ob.b);

        ob.meth(ob);

        System.out.println("ob.a и ob.b после вызова: " + ob.a + " " + ob.b);
    }
}
/*
Что демонстрирует код (передача объектов по ссылке):
В Java объекты передаются в методы по ссылке — метод получает ссылку на тот же объект, а не его копию. Изменения, сделанные внутри метода, влияют на исходный объект.

Выполнение:
Создаётся объект ob с полями a = 15, b = 20
Вызывается ob.meth(ob) — в метод передаётся ссылка на объект
Внутри метода: a становится 30, b становится 10
Исходный объект изменяется
*/
