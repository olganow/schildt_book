package chapter5;


// Использование break для выхода из вложенных циклов.
class BreakLoop4 {
    public static void main(String[] args) {
        outer: for (int i=0; i<3; i++) {
            System.out.print("Проход " + i + ": ");
            for (int j=0; j<100; j++) {
                if (j == 10) break outer; // выйти из обоих циклов
                System.out.print(j + " ");
            }
            System.out.println("Это выводиться не будет.");
        }
        System.out.println("Циклы завершены.");
    }
}
/*
Что демонстрирует код (меченный break для выхода из вложенных циклов):
Метка outer: позволяет оператору break outer выйти сразу из внешнего цикла, а не только из внутреннего.

Выполнение:
i = 0: выводится "Проход 0: "
Внутренний цикл выводит числа от 0 до 9
При j == 10 срабатывает break outer
Происходит выход из внешнего цикла с меткой outer
Строка "Это выводиться не будет" никогда не выполняется*/
