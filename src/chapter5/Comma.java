package chapter5;

// Использование запятой.
class Comma {
    public static void main(String[] args) {
        int a, b;

        for(a=1, b=4; a<b; a++, b--) {
            System.out.println("a = " + a);
            System.out.println("b = " + b);
        }
    }
}
/*
Что демонстрирует код (оператор "запятая" в цикле for):
Оператор "запятая" позволяет объединить несколько выражений в секциях инициализации и итерации цикла for:
Инициализация: a=1, b=4 (обе переменные инициализируются одновременно)
Итерация: a++, b-- (после каждой итерации a увеличивается, b уменьшается)
*/
