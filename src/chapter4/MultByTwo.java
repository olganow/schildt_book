package chapter4;
// Сдвиг влево как быстрый способ умножения на 2.
class MultByTwo {
    public static void main(String[] args) {
        int i;
        int num = 0xFFFFFFFF;
        for(i=0; i<4; i++) {
            num = num << 1;
            System.out.println(num);
        }
    }
}
/*
Что демонстрирует код:
0xFFFFFFFF — это 32-битное число, где все биты равны 1 (в десятичной системе это -1, так как int в Java знаковый, используется дополнительный код)
num = num << 1 — сдвиг влево на 1 бит (умножение на 2)
При сдвиге знакового int старший (знаковый) бит теряется, и число может стать положительным
*/
